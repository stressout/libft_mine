#include "libft.h"

size_t				ft_strlen(const char *s)
{
	size_t			len;
	unsigned char	*s1;

	len = 0;
	s1 = (unsigned char*)s;
	while (s1[len] != '\0')
	{
		len++;
	}
	return (len);
}
